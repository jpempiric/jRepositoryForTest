package jpackageforGit;

public class S11_Variable_1 {
	
	public static void main(String[] args) {
		int a = 5; // int type Local Variable
		System.out.println(a); // 5

		int x = 100;
		System.out.println(x); // 100

		x = 200;
		System.out.println(x); // 200

		char c = 'a'; // char type Local Variable, should be place inside single Quote
		System.out.println(c); // a

		boolean b = true;
		b = false;
		System.out.println(b); // false

		System.out.println(3 > 12); // false

		int e = 100; // int type Local Variable
		int f = 200; // int type Local Variable

		boolean z = e > f;
		System.out.println(z); // false

	}

}
